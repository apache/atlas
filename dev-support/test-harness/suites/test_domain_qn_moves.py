"""Domain/Product QualifiedName move/reparent tests.

Tests domain promotion (sub -> super), demotion (super -> sub),
cross-domain moves, product moves between domains, cycle prevention,
and multi-level cascade verification.  Verifies that the server-side
preprocessor cascades QN changes to all children.

Server-side QN patterns (DataDomainPreProcessor.java):
  SuperDomain: default/domain/{nanoId}/super
  SubDomain:   {parentDomainQN}/domain/{nanoId}
  Product:     {parentDomainQN}/product/{nanoId}

Key behaviors:
  - Promote (sub->super): nanoId preserved
  - Demote (super->sub): nanoId MAY be regenerated by the server
  - Cross-domain move (sub->sub): nanoId typically preserved via getOwnQualifiedNameForChild()
  - Child cascade: nanoIds preserved (last 2 path segments extracted)
"""

