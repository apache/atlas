# ---- QN helpers ----

# SuperDomain: default/domain/{nanoId}/super
_SUPER_DOMAIN_RE = re.compile(r"^default/domain/([^/]+)/super$")
# SubDomain: .../domain/{nanoId}  (parent prefix varies)
_SUB_DOMAIN_RE = re.compile(r"^(.+)/domain/([^/]+)$")
# Product: .../product/{nanoId}
_PRODUCT_RE = re.compile(r"^(.+)/product/([^/]+)$")


def _extract_super_nano(qn):
    """Extract nanoId from a super-domain QN, or None."""
    m = _SUPER_DOMAIN_RE.match(qn)
    return m.group(1) if m else None


def _extract_sub_nano(qn):
    """Return (parent_prefix, nanoId) from a sub-domain QN, or (None, None)."""
    m = _SUB_DOMAIN_RE.match(qn)
    return (m.group(1), m.group(2)) if m else (None, None)


def _extract_product_nano(qn):
    """Return (parent_prefix, nanoId) from a product QN, or (None, None)."""
    m = _PRODUCT_RE.match(qn)
    return (m.group(1), m.group(2)) if m else (None, None)


def _read_entity_qn(client, guid):
    """GET entity by GUID and return its qualifiedName (or None)."""
    resp = client.get(f"/entity/guid/{guid}")
    if resp.status_code != 200:
        return None
    return resp.json().get("entity", {}).get("attributes", {}).get("qualifiedName")


def _read_entity_attrs(client, guid):
    """GET entity by GUID and return its attributes dict (or {})."""
    resp = client.get(f"/entity/guid/{guid}")
    if resp.status_code != 200:
        return {}
    return resp.json().get("entity", {}).get("attributes", {})


def _wait_for_qn_change(client, guid, old_qn, max_wait=30, interval=3):
    """Poll until entity QN differs from old_qn, or timeout. Returns final QN."""
    deadline = time.time() + max_wait
    qn = old_qn
    while time.time() < deadline:
        qn = _read_entity_qn(client, guid)
        if qn and qn != old_qn:
            return qn
        time.sleep(interval)
    return qn


def _create_entity(client, entity_payload, label="entity"):
    """POST /entity and return (guid, resp) or raise SkipTestError."""
    for attempt in range(3):
        resp = client.post("/entity", json_data={"entity": entity_payload}, timeout=60)
        if resp.status_code != 408 or attempt == 2:
            break
        time.sleep(5 * (attempt + 1))

    if resp.status_code in (400, 403):
        raise SkipTestError(
            f"{label} creation returned {resp.status_code} — "
            f"Keycloak/preprocessor not configured on this environment"
        )
    assert_status(resp, 200)

    body = resp.json()
    creates = body.get("mutatedEntities", {}).get("CREATE", [])
    updates = body.get("mutatedEntities", {}).get("UPDATE", [])
    entities = creates or updates
    assert entities, f"{label} creation returned no entities in mutatedEntities"
    return entities[0]["guid"], resp


