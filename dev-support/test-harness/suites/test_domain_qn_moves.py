    @test("verify_sub_became_super", tags=["move", "data_mesh"], order=11,
          depends_on=["move_sub_to_super"])
    def test_verify_sub_became_super(self, client, ctx):
        """Verify promoted subdomain has super-domain QN pattern (with polling)."""
        guid = ctx.get_entity_guid("move_sub")
        old_nano = ctx.get("move_sub_nano")
        old_qn = ctx.get("move_sub_qn")

        # Poll for QN change
        new_qn = _wait_for_qn_change(client, guid, old_qn, max_wait=30)
        assert new_qn and new_qn != old_qn, (
            f"QN did not change after promote: still {new_qn}"
        )

        # Should now match default/domain/{nanoId}/super
        nano = _extract_super_nano(new_qn)
        assert nano, (
            f"Promoted domain QN does not match super-domain pattern, got: {new_qn}"
        )
        assert nano == old_nano, (
            f"NanoId changed during promote: expected {old_nano}, got {nano}"
        )

        # parentDomainQualifiedName should be null/empty
        attrs = _read_entity_attrs(client, guid)
        parent_qn = attrs.get("parentDomainQualifiedName")
        assert not parent_qn, (
            f"Expected null parentDomainQualifiedName after promote, got: {parent_qn}"
        )

        ctx.set("move_sub_qn", new_qn)
        print(f"  [domain-move] Verified promote: new QN={new_qn}")

    @test("verify_cascade_after_promote", tags=["move", "data_mesh"], order=12,
          depends_on=["verify_sub_became_super"])
    def test_verify_cascade_after_promote(self, client, ctx):
        """Verify sub-sub-domain and product QNs updated with new prefix."""
        new_sub_qn = ctx.get("move_sub_qn")  # now a super-domain QN
        old_subsub_nano = ctx.get("move_subsub_nano")
        old_prod_nano = ctx.get("move_prod_sub_nano")
        old_subsub_qn = ctx.get("move_subsub_qn")
        old_prod_qn = ctx.get("move_prod_sub_qn")

        # Check sub-sub-domain (poll for cascade)
        subsub_guid = ctx.get_entity_guid("move_subsub")
        subsub_qn = _wait_for_qn_change(client, subsub_guid, old_subsub_qn, max_wait=30)
        assert subsub_qn, f"Expected qualifiedName on sub-sub after cascade ({subsub_guid})"
        parent_prefix, nano = _extract_sub_nano(subsub_qn)
        assert parent_prefix == new_sub_qn, (
            f"Sub-sub parent prefix not cascaded: expected {new_sub_qn}, "
            f"got {parent_prefix} (full QN: {subsub_qn})"
        )
        assert nano == old_subsub_nano, (
            f"Sub-sub nanoId changed during cascade: expected {old_subsub_nano}, got {nano}"
        )
        ctx.set("move_subsub_qn", subsub_qn)
        print(f"  [domain-move] Sub-sub after promote: {subsub_qn}")

        # Check product under subdomain (poll for cascade)
        prod_guid = ctx.get_entity_guid("move_prod_sub")
        prod_qn = _wait_for_qn_change(client, prod_guid, old_prod_qn, max_wait=30)
        assert prod_qn, f"Expected qualifiedName on product after cascade ({prod_guid})"
        prod_prefix, prod_nano = _extract_product_nano(prod_qn)
        assert prod_prefix == new_sub_qn, (
            f"Product parent prefix not cascaded: expected {new_sub_qn}, "
            f"got {prod_prefix} (full QN: {prod_qn})"
        )
        assert prod_nano == old_prod_nano, (
            f"Product nanoId changed during cascade: expected {old_prod_nano}, got {prod_nano}"
        )
        ctx.set("move_prod_sub_qn", prod_qn)
        print(f"  [domain-move] Product after promote: {prod_qn}")

