    @test("try_cycle_parent_to_child", tags=["move", "data_mesh", "validation"],
          order=33, depends_on=["create_second_sub_under_a", "move_product_to_domain_a"])
    def test_try_cycle_parent_to_child(self, client, ctx):
        """VALIDATE: Moving A under Sub2 should be rejected (circular reference).

        Sub2 is a child of A. Moving A under Sub2 would create:
        Sub2 -> A -> Sub2 (cycle). The preprocessor should detect this
        and return 400 with a circular reference error.
        """
        a_guid = ctx.get_entity_guid("move_domain_a")
        a_qn = ctx.get("move_domain_a_qn")
        sub2_guid = ctx.get_entity_guid("move_sub2")
        assert a_guid and a_qn and sub2_guid, "Required entities not created"

        resp = client.post("/entity", json_data={
            "entity": {
                "typeName": "DataDomain",
                "guid": a_guid,
                "attributes": {
                    "qualifiedName": a_qn,
                    "name": self.domain_a_name,
                },
                "relationshipAttributes": {
                    "parentDomain": {
                        "guid": sub2_guid,
                        "typeName": "DataDomain",
                    },
                },
            }
        }, timeout=60)
        # Should be rejected — either 400 (circular reference) or 409 (conflict)
        assert resp.status_code in (400, 409), (
            f"Expected 400/409 for circular move attempt, got {resp.status_code}. "
            f"Server should prevent moving a parent domain under its own child."
        )
        print(f"  [domain-move] Cycle prevention: {resp.status_code} (expected rejection)")

